#include <unistd.h>

void	ft_putstr(char *str);

int	main(void)
{
	ft_putstr("Fernando");
	write(1, "\n", 1);
	return (0);
}

void	ft_putstr(char *str)
{
	int	index;

	index = 0;
	while (str[index] != '\0')
	{
		write(1, &str[index], 1);
		index++;
	}
}
